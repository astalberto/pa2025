        Cliente objCliente = new Cliente();
        LogCliente objLogCliente = new LogCliente();
        objCliente.setCedula(jTextCedula.getText());
        objCliente.setNombres(jTextNombre.getText());
        objCliente.setApellidos(jTextApellidos.getText());
