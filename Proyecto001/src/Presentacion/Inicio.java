    private void jButtonBuscarCedulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarCedulaActionPerformed
        Cliente objCliente = new Cliente();
        LogCliente objLogCliente = new LogCliente();
                objCliente.setCedula(this.jTextBuscarCedula.getText());
        try {
            objLogCliente.BuscarClienteCedula(objCliente);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(InsCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(InsCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (objCliente.getNombres().isEmpty())
            JOptionPane.showMessageDialog(null,"Cedula no encontrada");
        else {
            jTextMaxId.setText(String.valueOf(objCliente.getIdClie()));
            jTextCedula.setText(objCliente.getCedula());
            jTextNombre.setText(objCliente.getNombres());
            jTextApellidos.setText(objCliente.getApellidos());
        }
    }//GEN-LAST:event_jButtonBuscarCedulaActionPerformed

